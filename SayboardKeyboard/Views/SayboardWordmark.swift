  private static let parsedPaths: [[[PathCommand]]] = glyphPaths.map { Self.parseSVGPath($0) }

  private static func parseSVGPath(_ pathData: String) -> [[PathCommand]] {
    var result = [[PathCommand]]()
    var current = [PathCommand]()
    var nums = [CGFloat]()
    var numBuf = ""
    var lastCmd: Character = " "

    func flushNum() {
      if !numBuf.isEmpty, let val = Double(numBuf) {
        nums.append(CGFloat(val))
      }
      numBuf = ""
    }

    for ch in pathData {
      if ch == "M" || ch == "C" || ch == "L" || ch == "Z" {
        flushNum()
        if lastCmd != " " {
          Self.emitCommand(lastCmd, nums: &nums, current: &current, result: &result)
        }
        if ch == "Z" {
          Self.emitCommand("Z", nums: &nums, current: &current, result: &result)
          lastCmd = " "
        } else {
          lastCmd = ch
        }
      } else if ch == "," || ch == " " {
        flushNum()
      } else if ch == "-" {
        flushNum()
        numBuf.append(ch)
      } else {
        numBuf.append(ch)
      }
    }

    flushNum()
    if lastCmd != " " {
      Self.emitCommand(lastCmd, nums: &nums, current: &current, result: &result)
    }
    if !current.isEmpty {
      result.append(current)
    }
    return result
  }

  private static func emitCommand(
    _ cmd: Character,
    nums: inout [CGFloat],
    current: inout [PathCommand],
    result: inout [[PathCommand]],
  ) {
    switch cmd {
    case "M":
      while nums.count >= 2 {
        current.append(.moveTo(nums.removeFirst(), nums.removeFirst()))
      }

    case "C":
      while nums.count >= 6 {
        let x1 = nums.removeFirst()
        let y1 = nums.removeFirst()
        let x2 = nums.removeFirst()
        let y2 = nums.removeFirst()
        current.append(.curveTo(x1, y1, x2, y2, nums.removeFirst(), nums.removeFirst()))
      }

    case "L":
      while nums.count >= 2 {
        current.append(.lineTo(nums.removeFirst(), nums.removeFirst()))
      }

    case "Z":
      current.append(.close)
      result.append(current)
      current = []

    default:
      break
    }
  }
