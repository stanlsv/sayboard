  func path(in rect: CGRect) -> Path {
    let parsed = Self.parsedPaths
    let scaleX = rect.width / Self.viewBoxWidth
    let scaleY = rect.height / Self.viewBoxHeight
    let scale = min(scaleX, scaleY)
    let offsetX = rect.minX + (rect.width - Self.viewBoxWidth * scale) / 2
    let offsetY = rect.minY + (rect.height - Self.viewBoxHeight * scale) / 2

    var result = Path()
    for subpaths in parsed {
      for subpath in subpaths {
        for (index, cmd) in subpath.enumerated() {
          switch cmd {
          case .moveTo(let x, let y):
            result.move(to: CGPoint(x: offsetX + x * scale, y: offsetY + y * scale))

          case .curveTo(let x1, let y1, let x2, let y2, let x, let y):
            result.addCurve(
              to: CGPoint(x: offsetX + x * scale, y: offsetY + y * scale),
              control1: CGPoint(x: offsetX + x1 * scale, y: offsetY + y1 * scale),
              control2: CGPoint(x: offsetX + x2 * scale, y: offsetY + y2 * scale),
            )

          case .close:
            result.closeSubpath()

          case .lineTo(let x, let y):
            if index == 0 {
              result.move(to: CGPoint(x: offsetX + x * scale, y: offsetY + y * scale))
            } else {
              result.addLine(to: CGPoint(x: offsetX + x * scale, y: offsetY + y * scale))
            }
          }
        }
      }
    }
    return result
  }

